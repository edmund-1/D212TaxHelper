async function main() {
  if (!fs.existsSync(OUT_DIR)) {
    fs.mkdirSync(OUT_DIR, { recursive: true });
  }

  const dividendsPath = path.join(OUT_DIR, 'xtb-dividends-anonymized.pdf');
  const portfolioPath = path.join(OUT_DIR, 'xtb-portfolio-anonymized.pdf');

  await generateDividendsPdf(dividendsPath);
  console.log('Generated', dividendsPath);

  await generatePortfolioPdf(portfolioPath);
  console.log('Generated', portfolioPath);
}

main().catch(err => {
  console.error('Failed to generate test PDFs:', err);
  process.exit(1);
});
