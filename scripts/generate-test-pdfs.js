#!/usr/bin/env node
/**
 * Generate anonymized XTB report PDFs for use as test fixtures.
 *
 * The two PDFs produced under test/fixtures/ mirror the *layout* (column
 * positions, line ordering, header phrases) of real XTB Romania reports —
 * just enough that pdf-parse-new extracts text in the same shape and our
 * parsers exercise their real code paths — but contain ONLY synthetic data.
 *
 * Hardening:
 *   - PDF /Info dictionary is set explicitly (no leaking author / producer
 *     from the system that runs this script).
 *   - CreationDate / ModDate are pinned to a fixed constant so re-running
 *     the generator yields byte-identical PDFs (good for git).
 *   - PDFKit emits no XMP metadata by default; we still avoid Document.xmp.
 *   - No personal data (names, CNPs, addresses, account numbers, real
 *     amounts) appears anywhere in the generated bytes.
 *
 * Usage: node scripts/generate-test-pdfs.js
 *
 * The generated PDFs are committed to the repo so contributors don't need
 * pdfkit installed to run the tests. Regenerate only when you change the
 * fixture content.
 */

const fs = require('fs');
const path = require('path');
const PDFDocument = require('pdfkit');

const OUT_DIR = path.join(__dirname, '..', 'test', 'fixtures');
const FIXED_DATE = new Date('2025-01-15T10:00:00.000Z');

const PDF_INFO = {
  Title: 'D212TaxHelper Test Fixture',
  Author: 'D212TaxHelper Test Suite',
  Subject: 'Synthetic XTB report for automated parser testing',
  Keywords: 'test fixture anonymized',
  Creator: 'D212TaxHelper',
  Producer: 'D212TaxHelper',
  CreationDate: FIXED_DATE,
  ModDate: FIXED_DATE,
};

function makeDoc() {
  return new PDFDocument({
    size: 'A4',
    margin: 50,
    info: { ...PDF_INFO },
    // Avoid any auto-detected font that could carry system info.
    // PDFKit defaults to Helvetica which is a PDF core font (no embedding).
  });
}

