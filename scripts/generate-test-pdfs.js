  doc.moveDown();

  // Dividends header
  doc.fontSize(9);
  doc.text('Nr.');
  doc.text('Crt.');
  doc.text('Valoare bruta');
  doc.text('Dividende');
  doc.text('(in RON)');
  doc.text('Impozit');
  doc.text('retinut la');
  doc.text('sursa*)');
  doc.text('(in RON)');
  doc.text('Valoare neta');
  doc.text('Dividende');
  doc.text('(in RON)');
